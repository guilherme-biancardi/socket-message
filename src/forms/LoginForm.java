    private void buttonComponent1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonComponent1MouseClicked
        FieldInterface[] fields = {emailField, passwordField};

        if (Validation.validate(fields)) {
            try {
                String email = emailField.getText();
                String password = new String(passwordField.getPassword());

                loginController.authenticate(email, password);

                Pages.redirect(PageEnum.CHAT, this);
            } catch (SQLException ex) {
                Alert.error("Usuário inválido", "Email e/ou senha inválidos");
            } catch (IOException ex) {
                try {
                    throw new ServerErrorException(ex.getMessage());
                } catch (ServerErrorException ex1) {
                }
            }
        }
    }//GEN-LAST:event_buttonComponent1MouseClicked

