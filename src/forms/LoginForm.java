    private void changeTheme() {
        Colors.setTheme(Colors.isDarkTheme() ? ColorTheme.LIGHT : ColorTheme.DARK);

        String password = new String(passwordField.getPassword());

        LoginForm loginForm = new LoginForm(emailField.getText(), password);

        String imageName = Colors.isDarkTheme() ? "sunny" : "night";
        String iconThemePath = "/assets/weather-" + imageName + ".png";

        loginForm.buttonTheme.setIcon(new ImageIcon(getClass().getResource(iconThemePath)));

        Pages.setPage(PageEnum.LOGIN, loginForm);
        Pages.setPage(PageEnum.REGISTER, new RegisterForm());

        Pages.redirect(PageEnum.LOGIN, this);
    }

